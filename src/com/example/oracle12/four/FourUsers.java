package com.example.oracle12.four;

public class FourUsers {
	private int head;
	private String name;

	public FourUsers() {
		super();
	}

	public FourUsers(int head, String name) {
		super();
		this.head = head;
		this.name = name;
	}

	public int getHead() {
		return head;
	}

	public void setHead(int head) {
		this.head = head;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
